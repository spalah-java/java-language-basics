package ua.spalah.basics.datatypes;

public class Variables {

    public static void main(String[] args) {
        // Можно объявить и инициализировать переменную одновременно:
        int salary = 3000;
        long height = 9999999L;

        // А можно отдельно
        String s;
        s = "Hello";

        // Для констант используется ключевое слово final
        final double CM_PER_INCH = 2.54;
    }
}
